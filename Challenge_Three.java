public class Challenge_Three
{
    public static void main(String[] args)
        {
            int marks = 68;

            if (marks >= 80)
                {
                    System.out.println("Grade: A");
                }
            else if (marks >= 70)
                {
                    System.out.println("Grade: B");
                }
            else if (marks >= 60)
                {
                    System.out.println("Grade: C");
                }
            else if (marks >= 50)
                {
                    System.out.println("Grade: D");
                }
            else
                {
                    System.out.println("Grade: F (Fail)");
                }
        }
}

